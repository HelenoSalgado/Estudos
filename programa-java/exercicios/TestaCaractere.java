public class TestaCaractere {

  public static void main(String[] args) {

    // O char só recebe um caractere, ele é do tipo numérico, apesar de aceitar uma letra.

    char letra = 'a';

    System.out.println(letra);

    // String não é um tipo propriamente dito.

    String palavra = "Eu o sou.";

    System.out.println(palavra);

  }
}